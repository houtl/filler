#include "filler.h"

void	free_tab(t_env *e)
{
	int		j;

	j = -1;
	while (++j < e->y)
		free(e->tab[j]);
	free(e->tab);
}

void	free_piece(t_env *e)
{
	int		j;

	j = -1;
	while (++j < e->yp)
		free(e->piece[j]);
	free(e->piece);
}
